def plot_parameter_sensitivity(theta_data, dataset_name, h=5.0):
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    fig.suptitle(f'Task 3 — Parameter Sensitivity\n{dataset_name}  |  h={h}s',
                 fontsize=13, fontweight='bold')

    ax1, ax2 = axes
    t_data = np.arange(len(theta_data)) * dt_data

    # Vary alpha (k fixed)
    alpha_values = [0.01, 0.05, 0.1, 0.3]
    colors_a     = ['#264653', '#2a9d8f', '#e9c46a', '#e76f51']
    k_fixed      = 0.01
    for alpha_val, color in zip(alpha_values, colors_a):
        t, T, _ = solve_task3(theta_data, h=h, alpha=alpha_val, k=k_fixed)
        ax1.plot(t, T, label=f'α = {alpha_val}', color=color, linewidth=1.8)
    ax1.plot(t_data, theta_data, 'k--', linewidth=1, label='θ(t) outside', alpha=0.5)
    ax1.set_xlabel('Time (seconds)')
    ax1.set_ylabel('Temperature (°F)')
    ax1.set_title(f'Varying α  (k = {k_fixed} fixed)')
    ax1.legend(fontsize=9)
    ax1.grid(True, alpha=0.3)

    # Vary k (alpha fixed)
    k_values    = [0.001, 0.01, 0.05, 0.1]
    colors_k    = ['#3a0ca3', '#4361ee', '#4cc9f0', '#f72585']
    alpha_fixed = 0.05
    for k_val, color in zip(k_values, colors_k):
        t, T, _ = solve_task3(theta_data, h=h, alpha=alpha_fixed, k=k_val)
        ax2.plot(t, T, label=f'k = {k_val}', color=color, linewidth=1.8)
    ax2.plot(t_data, theta_data, 'k--', linewidth=1, label='θ(t) outside', alpha=0.5)
    ax2.set_xlabel('Time (seconds)')
    ax2.set_ylabel('Temperature (°F)')
    ax2.set_title(f'Varying k  (α = {alpha_fixed} fixed)')
    ax2.legend(fontsize=9)
    ax2.grid(True, alpha=0.3)

    plt.tight_layout()
    plt.show()
    print(f"Saved sensitivity plot for {dataset_name}")


