def plot_timestep_effect(theta_data, dataset_name, alpha=0.05, k=0.01):
    h_values = [10.0, 5.0, 2.0, 1.0]
    colors   = ['#e63946', '#457b9d', '#2a9d8f', '#e9c46a']

    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    fig.suptitle(f'Task 3 — Effect of Time-Step Size\n{dataset_name}  |  α={alpha}, k={k}',
                 fontsize=13, fontweight='bold')

    ax1, ax2 = axes

    for h, color in zip(h_values, colors):
        t, T, _ = solve_task3(theta_data, h=h, alpha=alpha, k=k)
        ax1.plot(t, T, label=f'h = {h}s', color=color, linewidth=1.5)

    t_data = np.arange(len(theta_data)) * dt_data
    ax1.plot(t_data, theta_data, 'k--', linewidth=1, label='θ(t) outside', alpha=0.5)
    ax1.set_xlabel('Time (seconds)')
    ax1.set_ylabel('Temperature (°F)')
    ax1.set_title('T(t) for different step sizes h')
    ax1.legend(fontsize=9)
    ax1.grid(True, alpha=0.3)

    # Difference plot: compare each h to the finest h=1
    t_ref, T_ref, _ = solve_task3(theta_data, h=1.0, alpha=alpha, k=k)
    for h, color in zip(h_values[:-1], colors[:-1]):
        t, T, _ = solve_task3(theta_data, h=h, alpha=alpha, k=k)
        T_interp = np.interp(t_ref, t, T)
        ax2.plot(t_ref, np.abs(T_interp - T_ref),
                 label=f'|T_h={h} − T_h=1|', color=color, linewidth=1.5)

    ax2.set_xlabel('Time (seconds)')
    ax2.set_ylabel('|Error| (°F)')
    ax2.set_title('Difference from finest solution (h=1s)')
    ax2.legend(fontsize=9)
    ax2.grid(True, alpha=0.3)

    plt.tight_layout()
    plt.show()
    print(f"Saved timestep plot for {dataset_name}")


