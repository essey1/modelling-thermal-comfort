alpha   = 0.05    # thermal inertia coefficient
k       = 0.01    # heat-transfer coefficient
T0      = 72.0    # initial temperature (°F)
dt_data = 10.0    # seconds between measurements


def u(t, k):
    """Heating/cooling input from climate control system."""
    return 0.1 * k * np.sin(np.pi * t / 10)


# ──────────────────────────────────────────────
# TASK 3 SOLVER
# ──────────────────────────────────────────────

def solve_task3(theta_data, h, alpha, k, T0=72.0, T_prime0=0.0, dt_data=10.0):
    """
    Solve the second-order ODE using centered finite differences.

    Equation:
        T''(t) + alpha * T'(t) = -k*(T(t) - theta(t)) + u(t)

    Centered finite difference approximations:
        T''_i ≈ (T_{i+1} - 2*T_i + T_{i-1}) / h^2
        T'_i  ≈ (T_{i+1} - T_{i-1}) / (2h)

    Substituting and multiplying through by h^2:
        (1 + ah/2)*T_{i+1}  -  (2 - h^2*k)*T_i  +  (1 - ah/2)*T_{i-1}
            = h^2*k*theta_i + h^2*u_i

    Row 1 uses the ghost-node trick for T'(0) = T_prime0:
        Central diff at i=0: (T_1 - T_{-1}) / (2h) = T_prime0
            => T_{-1} = T_1 - 2h*T_prime0
        Substituting into the i=0 interior equation gives:
            2*T_1 = (2 - h^2*k)*T0 + h^2*k*theta_0 + h^2*u_0
                    + 2h*T_prime0*(1 - ah/2)

    Parameters
    ----------
    theta_data : array of external temperatures (uniformly spaced at dt_data)
    h          : time step for the finite difference solver (seconds)
    alpha      : thermal inertia coefficient
    k          : heat transfer coefficient
    T0         : initial condition T(0)
    T_prime0   : initial condition T'(0), default 0 (no initial rate of change)
    dt_data    : sampling interval of theta_data (seconds), default 10.0
    """
    # ── Build time grids ──────────────────────────────────────────────────────
    N_data   = len(theta_data)
    t_data   = np.arange(N_data) * dt_data       # original time points
    t_end    = t_data[-1]
    t_solver = np.arange(0, t_end + h, h)         # solver time points
    N        = len(t_solver)

    # ── Interpolate theta onto solver grid ────────────────────────────────────
    cs      = CubicSpline(t_data, theta_data)
    theta_s = cs(t_solver)

    # ── Control input on solver grid (FIX: renamed to u_arr to avoid shadowing u()) ──
    u_arr = np.array([u(t_solver[i], k) for i in range(N)])

    # ── Centered-difference stencil coefficients ─────────────────────────────
    cl = 1.0 - alpha * h / 2.0    # coefficient of T_{i-1}
    cd = -(2.0 - h**2 * k)        # coefficient of T_i
    cr = 1.0 + alpha * h / 2.0    # coefficient of T_{i+1}

    # ── Build A and b ─────────────────────────────────────────────────────────
    A = np.zeros((N, N))
    b = np.zeros(N)

    # Row 0: T_0 = T0  (Dirichlet condition)
    A[0, 0] = 1.0
    b[0]    = T0

    # Row 1: ghost-node equation at i=0
    #   2*T_1 = (2 - h^2*k)*T0 + h^2*k*theta_0 + h^2*u_0
    #           + 2h*T_prime0*(1 - ah/2)
    A[1, 1] = 2.0
    b[1]    = ((2.0 - h**2 * k) * T0
               + h**2 * k * theta_s[0]
               + h**2 * u_arr[0]
               + 2.0 * h * T_prime0 * cl)    # vanishes when T_prime0 = 0

    # Rows 2 … N-1: interior centered-difference equation at node (i-1)
    #   cl*T_{i-2}  +  cd*T_{i-1}  +  cr*T_i  =  h^2*k*theta_{i-1} + h^2*u_{i-1}
    for i in range(2, N):
        A[i, i - 2] = cl
        A[i, i - 1] = cd
        A[i, i    ] = cr
        b[i]        = h**2 * k * theta_s[i - 1] + h**2 * u_arr[i - 1]

    # ── Solve the linear system ───────────────────────────────────────────────
    T = np.linalg.solve(A, b)
    return t_solver, T, theta_s    # FIX: return theta_s so callers can unpack 3 values


